import fs from "node:fs";
import path from "node:path";
import { pathToFileURL } from "node:url";
import { writeTextFileIfChanged } from "./runtime-postbuild-shared.mjs";

const GENERATED_BY = "scripts/generate-host-env-security-policy-swift.mjs";
const POLICY_JSON_PATH = "src/infra/host-env-security-policy.json";
const DEFAULT_OUTPUT_PATH =
  "apps/macos/Sources/Deneb/Generated/HostEnvSecurityPolicy.generated.swift";

function readPolicy(repoRoot) {
  const policyPath = path.resolve(repoRoot, POLICY_JSON_PATH);
  return JSON.parse(fs.readFileSync(policyPath, "utf8"));
}

function renderSwiftStringArray(items, indent) {
  if (items.length === 0) {
    return "[]";
  }
  const entries = items.map((item) => `${indent}  ${JSON.stringify(item)},`).join("\n");
  return `[\n${entries}\n${indent}]`;
}

export function renderHostEnvSecurityPolicySwift(policy) {
  const blockedKeys = renderSwiftStringArray(policy.blockedKeys, "  ");
  const blockedPrefixes = renderSwiftStringArray(policy.blockedPrefixes, "  ");
  const blockedOverrideKeys = renderSwiftStringArray(policy.blockedOverrideKeys ?? [], "  ");
  const blockedOverridePrefixes = renderSwiftStringArray(
    policy.blockedOverridePrefixes ?? [],
    "  ",
  );

  return `// Auto-generated by ${GENERATED_BY}. Do not edit directly.

import Foundation

enum HostEnvSecurityPolicy {
  static let blockedKeys: Set<String> = Set(${blockedKeys})

  static let blockedPrefixes: [String] = ${blockedPrefixes}

  static let blockedOverrideKeys: Set<String> = Set(${blockedOverrideKeys})

  static let blockedOverridePrefixes: [String] = ${blockedOverridePrefixes}
}
`;
}

export function writeHostEnvSecurityPolicySwift(params = {}) {
  const repoRoot = path.resolve(params.repoRoot ?? process.cwd());
  const outputPath = path.resolve(repoRoot, params.outputPath ?? DEFAULT_OUTPUT_PATH);
  const policy = readPolicy(repoRoot);
  const next = renderHostEnvSecurityPolicySwift(policy);

  if (params.check) {
    if (!fs.existsSync(outputPath)) {
      // Output file does not exist yet (e.g. macOS app not in this checkout); nothing to drift.
      return { changed: false, wrote: false, outputPath };
    }
    const current = fs.readFileSync(outputPath, "utf8");
    return { changed: current !== next, wrote: false, outputPath };
  }

  return {
    changed: true,
    wrote: writeTextFileIfChanged(outputPath, next),
    outputPath,
  };
}

if (import.meta.url === pathToFileURL(process.argv[1] ?? "").href) {
  const isCheck = process.argv.includes("--check");
  const result = writeHostEnvSecurityPolicySwift({ check: isCheck });

  if (result.changed) {
    if (isCheck) {
      console.error(
        `[host-env-security-policy-swift] stale generated output at ${path.relative(process.cwd(), result.outputPath)}`,
      );
      process.exitCode = 1;
    } else {
      console.log(
        `[host-env-security-policy-swift] wrote ${path.relative(process.cwd(), result.outputPath)}`,
      );
    }
  }
}
